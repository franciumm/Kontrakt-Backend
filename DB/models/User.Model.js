import mongoose from 'mongoose';

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
      lowercase: true,
    },
    password: {
      type: String,
      required: true,
    },
    name: {
      type: String,
      required: true,
      trim: true,
    },
    role: {
      type: String,
      enum: ['freelancer', 'admin'],
      default: 'freelancer',
    },
  },
  {
    timestamps: true,
  }
);

export const User = mongoose.model('User', userSchema);
